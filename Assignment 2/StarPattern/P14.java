class P14{
public static void main(String args[]){
int i,j;
for(i=1;i<5;i++){
	for(j=0;j<=i;j++){
	System.out.print(" ");
	}

for(j=i;j<=5;j++){
System.out.print("*"+" ");
}
System.out.println(" ");
}


for(i=5;i>=1;i--){
	for(j=0;j<=i;j++){
	System.out.print(" ");
	}

for(j=i;j<=5;j++){
System.out.print("*"+" ");
}
System.out.println(" ");
}
}


}